$('.form-line').on('input',function() {
  var vname = $('#vname').val();
  var vnameVal = +vname+'%3B';
  var number = $('#number').val();
  var numberVal = +number+'%3B';
  var place = $('#place').val();
  var placeVal = +place+'%3B';  
  var phone = $('#phone').val();
  var phoneVal = +phone+'%3B'; 
  var email = $('#email').val();
  var emailVal = +email+'%3B';
  var vehicle = $('#vehicle').val();  
  var vehicleVal = +vehicle+'%3B';   
  var material = $('#material').val();
  var materialVal = +material+'%3B';
  var search = $('#search').val();
  var searchVal = +search+'%3B';
  var purpose = $('#purpose').val();
  var purposeVal = +purpose+'%3B';
  var college = $('#college').val();
  var collegeVal = +college+'%3B'; 
  var vfdate = $('#vfdate').val();
  var vfdateVal = +vfdate+'%3B'; 
  var vtdate = $('#vtdate').val();
  var vtdateVal = +vtdate+'%3B'; 
  var qrUrlStart = 'https://api.qrserver.com/v1/create-qr-code/?data=VISITORPASS%0A';
  var qrUrlEnd = 'Developed By%3AT-HUB%0A';  
  var qrURL = qrUrlStart + vnameVal + numberVal + placeVal + phoneVal + emailVal + vehicleVal + materialVal + searchVal + purposeVal + collegeVal + vfdateVal + vtdateVal + qrUrlEnd;  
  $('.qr-img').attr('src',qrURL);    
  //$('.qr-img').attr('src',qrURL);  
  $('.biscard-out').html('<div class="email"><a href="mailto:'+email+'">'+email+'</a></div>');
});